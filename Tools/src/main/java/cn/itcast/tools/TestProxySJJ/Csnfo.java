package cn.itcast.tools.TestProxySJJ;

public class Csnfo {
    private Env env;
    private String hsfGroup;
    private String method;
    private Object[] parameter;
    private String hsfVersion;
    private String service;
    private String result;
    private String ip;
    private String  appName;

    public Csnfo() {
    }

    public Env getEnv() {
        return env;
    }

    public void setEnv(Env env) {
        this.env = env;
    }

    public String getHsfGroup() {
        return hsfGroup;
    }

    public void setHsfGroup(String hsfGroup) {
        this.hsfGroup = hsfGroup;
    }

    public String getMethod() {
        return method;
    }

    public void setMethod(String method) {
        this.method = method;
    }

    public Object[] getParameter() {
        return parameter;
    }

    public void setParameter(Object[] parameter) {
        this.parameter = parameter;
    }

    public String getHsfVersion() {
        return hsfVersion;
    }

    public void setHsfVersion(String hsfVersion) {
        this.hsfVersion = hsfVersion;
    }

    public String getService() {
        return service;
    }

    public void setService(String service) {
        this.service = service;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getAppName() {
        return appName;
    }

    public void setAppName(String appName) {
        this.appName = appName;
    }
}
